-- Tạo bảng
-- Bảng KHACH_HANG
CREATE TABLE KHACH_HANG (
	ID INT IDENTITY(1,1),
    MaKH AS 
		'KHHG' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    TenKH NVARCHAR(100),
    LoaiKH NVARCHAR(12),
    SDT CHAR(12),
    DiaChi NVARCHAR(100),
    Email VARCHAR(100)
);

-- Bảng THI_SINH
CREATE TABLE THI_SINH (
	ID INT IDENTITY(1,1),
    MaTS AS 
		'THSH' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    TenTS NVARCHAR(100),
    NgaySinh DATE,
    CCCD CHAR(12),
    MaKH VARCHAR(10)
);

-- Bảng NHAN_VIEN
CREATE TABLE NHAN_VIEN (
	ID INT IDENTITY(1,1),
    MaNV AS 
		'NHVN' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    TenNhanVien NVARCHAR(100),
    NgaySinh DATE,
    Email VARCHAR(100),
    SDT CHAR(12),
    DiaChi NVARCHAR(100),
    CCCD CHAR(12),
    LoaiNhanVien NVARCHAR(12)
);

-- Bảng PHONG_THI
CREATE TABLE PHONG_THI (
	ID INT IDENTITY(1,1),
    MaPT AS 
		'PHTI' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    SucChua INT,
    TrangThai NVARCHAR(12)
);

-- Bảng LOAI_CHUNG_CHI
CREATE TABLE LOAI_CHUNG_CHI (
	ID INT IDENTITY(1,1),
    MaLCC AS 
		'LOCC' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    TenLoaiChungChi NVARCHAR(100),
	LinhVucChungChi NVARCHAR(100),
    DiemChuan FLOAT,
    ThoiHan INT -- tháng
);

-- Bảng XEP_HANG_CHUNG_CHI
CREATE TABLE XEP_HANG_CHUNG_CHI (
    MaLCC VARCHAR(10),
    XepHang VARCHAR(10),
    DiemSan FLOAT,
    DiemTran FLOAT,
    PRIMARY KEY (MaLCC, XepHang)
);

-- Bảng LICH_THI
CREATE TABLE LICH_THI (
	ID INT IDENTITY(1,1),
    MaLT AS 
		'LICH' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    NgayGioThi DATETIME,
    SoLuongToiDa INT,
	SoLuongDaDangKy INT,
    LoaiChungChi VARCHAR(10),    
    MaPT VARCHAR(10)
);

-- Bảng NHAN_VIEN_GIAM_THI_LICH_THI
CREATE TABLE NHAN_VIEN_GIAM_THI_LICH_THI (
    MaLT VARCHAR(10),
    MaNV VARCHAR(10),
    VaiTro NVARCHAR(100), -- Ví dụ: Giám thị chính, Giám thị hành lang, Thư ký, ...
    PRIMARY KEY (MaLT, MaNV)
);

-- Bảng PHIEU_DANG_KY
CREATE TABLE PHIEU_DANG_KY (
	ID INT IDENTITY(1,1),
    MaPDK AS 
		'PHDK' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    NgayLapPDK DATE,
    TrangThai NVARCHAR(12),
    HanThanhToan DATE,
    MaLCC VARCHAR(10),
    MaLT VARCHAR(10),
    MaKH VARCHAR(10),
    MaTS VARCHAR(10),
    MaNVTiepNhan VARCHAR(10)
);

-- Bảng HOA_DON
CREATE TABLE HOA_DON (
	ID INT IDENTITY(1,1),
    MaHD AS 
		'HDON' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    NgayGioThanhToan DATETIME,
    SoTienThanhToan FLOAT,
    HinhThucThanhToan NVARCHAR(100),
    ChietKhau FLOAT,
    MaPDK VARCHAR(10),
    MaNVKeToan VARCHAR(10)
);

-- Bảng PHIEU_DU_THI
CREATE TABLE PHIEU_DU_THI (
	ID INT IDENTITY(1,1),
    MaPDT AS 
		'PHDT' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    NgayPhatHanh DATE,
    TrangThai NVARCHAR(12),
    ThoiGianThi DATETIME,
    DiaDiemThi NVARCHAR(100),
    MaTS VARCHAR(10),
    MaLT VARCHAR(10),
    MaHD VARCHAR(10)
);

-- Bảng KET_QUA_THI
CREATE TABLE KET_QUA_THI (
	ID INT IDENTITY(1,1),
    MaKQT AS 
		'KQTH' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    DiemSo FLOAT,
    NgayCoDiem DATE,
    MaTS VARCHAR(10),
    MaLCC VARCHAR(10),
	MaPDT VARCHAR(10)
);

-- Bảng CHUNG_CHI
CREATE TABLE CHUNG_CHI (
	ID INT IDENTITY(1,1),
    MaCC AS 
		'CHCH' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    DiemSo FLOAT,
    XepHang VARCHAR(10),
    NgayCap DATE,
    NgayHetHan DATE,
	TrangThai NVARCHAR(12),
    MaKQT VARCHAR(10),
    MaTS VARCHAR(10),
    MaLCC VARCHAR(10)
);

-- Bảng PHIEU_DU_THI_GIA_HAN
CREATE TABLE PHIEU_DU_THI_GIA_HAN (
	ID INT IDENTITY(1,1),
    MaPDTGH AS 
		'DTGH' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    MaPDT VARCHAR(10),
    SoLanConLai INT,
    HanGiaHan DATE
);

-- Bảng YEU_CAU_GIA_HAN
CREATE TABLE YEU_CAU_GIA_HAN (
	ID INT IDENTITY(1,1),
    MaYCGH AS 
		'YCGH' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    NgayYeuCauGiaHan DATE,
    LyDoGiaHan NVARCHAR(100),
    PhiGiaHan FLOAT,
    MaPDT VARCHAR(10)
);

-- Bảng HOA_DON_GIA_HAN
CREATE TABLE HOA_DON_GIA_HAN (
	ID INT IDENTITY(1,1),
    MaHDGH AS 
		'HDGH' + RIGHT('000000' + CAST(ID AS VARCHAR(6)), 6) 
		PERSISTED PRIMARY KEY,
    NgayGioThanhToan DATETIME,
    SoTienThanhToan FLOAT,
    HinhThucThanhToan NVARCHAR(100),
    TrangThai NVARCHAR(12),
    MaPDT VARCHAR(10),
    MaYCGH VARCHAR(10),
    MaNVKeToan VARCHAR(10)
);

