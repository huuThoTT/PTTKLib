-- PHIEU_DU_THI -> THI_SINH
ALTER TABLE PHIEU_DU_THI ADD CONSTRAINT FK_PDT_THI_SINH
FOREIGN KEY (MaTS) REFERENCES THI_SINH(MaTS);
-- PHIEU_DU_THI -> LICH_THI
ALTER TABLE PHIEU_DU_THI ADD CONSTRAINT FK_PDT_LICH_THI
FOREIGN KEY (MaLT) REFERENCES LICH_THI(MaLT);
-- PHIEU_DU_THI -> HOA_DON
ALTER TABLE PHIEU_DU_THI ADD CONSTRAINT FK_PDT_HOA_DON
FOREIGN KEY (MaHD) REFERENCES HOA_DON(MaHD);

-- KET_QUA_THI -> THI_SINH
ALTER TABLE KET_QUA_THI ADD CONSTRAINT FK_KQT_THI_SINH
FOREIGN KEY (MaTS) REFERENCES THI_SINH(MaTS);
-- KET_QUA_THI -> LOAI_CHUNG_CHI
ALTER TABLE KET_QUA_THI ADD CONSTRAINT FK_KQT_LOAI_CHUNG_CHI
FOREIGN KEY (MaLCC) REFERENCES LOAI_CHUNG_CHI(MaLCC);
-- KET_QUA_THI -> PHIEU_DU_THI
ALTER TABLE KET_QUA_THI ADD CONSTRAINT FK_KQT_PHIEU_DU_THI
FOREIGN KEY (MaPDT) REFERENCES PHIEU_DU_THI(MaPDT);

-- CHUNG_CHI -> KET_QUA_THI
ALTER TABLE CHUNG_CHI ADD CONSTRAINT FK_CHUNG_CHI_KET_QUA_THI
FOREIGN KEY (MaKQT) REFERENCES KET_QUA_THI(MaKQT);
-- CHUNG_CHI -> THI_SINH
ALTER TABLE CHUNG_CHI ADD CONSTRAINT FK_CHUNG_CHI_THI_SINH
FOREIGN KEY (MaTS) REFERENCES THI_SINH(MaTS);
-- CHUNG_CHI -> LOAI_CHUNG_CHI
ALTER TABLE CHUNG_CHI ADD CONSTRAINT FK_CHUNG_CHI_LOAI_CHUNG_CHI
FOREIGN KEY (MaLCC) REFERENCES LOAI_CHUNG_CHI(MaLCC);

